export function createDemoAnswer(
  text: string,
  topic: Topic,
  intensity: Intensity,
) {
  if (topic === "care") return careDemo(text);
  if (topic === "application") return applicationDemo(text, intensity);
  if (topic === "postgraduate") return postgraduateDemo(intensity);
  if (topic === "study") {
    return /考研|读研|研究生|硕士/.test(text)
      ? postgraduateDemo(intensity)
      : applicationDemo(text, intensity);
  }
  if (topic === "work") return workDemo(text, intensity);
  if (topic === "reply") return replyDemo(intensity);
  if (topic === "general") return generalDemo(text, intensity);
  return roastDemo(intensity);
}

export function cleanModelOutput(value: string) {
  return value
    .replace(/^```(?:text|markdown)?\s*/i, "")
    .replace(/\s*```$/i, "")
    .replace(/^(回答|张老师说|雪峰老师说)[：:]\s*/i, "")
    .trim()
    .slice(0, 2400);
}
