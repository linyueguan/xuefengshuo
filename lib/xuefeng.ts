export function cleanModelOutput(value: string) {
  return value
    .replace(/^```(?:text|markdown)?\s*/i, "")
    .replace(/\s*```$/i, "")
    .replace(/^(回答|张老师说|雪峰老师说)[：:]\s*/i, "")
    .trim()
    .slice(0, 3200);
}
